package beens;


public class KuchiBean {
	private int kCode;
	private int eCode;
	private String userid;
	private String des;
	private String date;
	private int hoshi;
	private String userNick;
	public String getUserNick() {
		return userNick;
	}
	public void setUserNick(String userNick) {
		this.userNick = userNick;
	}
	public int getkCode() {
		return kCode;
	}
	public void setkCode(int kCode) {
		this.kCode = kCode;
	}
	public int geteCode() {
		return eCode;
	}
	public void seteCode(int eCode) {
		this.eCode = eCode;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getDes() {
		return des;
	}
	public void setDes(String des) {
		this.des = des;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getHoshi() {
		return hoshi;
	}
	public void setHoshi(int hoshi) {
		this.hoshi = hoshi;
	}

}
